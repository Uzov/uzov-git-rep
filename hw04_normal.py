# произвольными целыми цифрами, в результате в файле должно быть
# 2500-значное произвольное число.
# Найдите и выведите самую длинную последовательность одинаковых цифр
# в вышезаполненном файле.
import os
import random

MAX = 2500

temp = list()
path = os.path.join('data', 'numbers.txt')
if os.path.exists(path):
    print("Файл numbers.txt уже существует!")
else:
    with open(path, 'w', encoding='utf-8') as file_f:
        temp = (random.randint(0, 9) for _ in range(0, MAX))
        string1 = ''.join(str(itm) for itm in temp)  # Перевод list в string
        file_f.write(string1)
with open(path, 'r', encoding='utf-8') as file_f:
    temp = file_f.read()

pattern = r'[0]{2,2500}|[1]{2,2500}|[2]{2,2500}|[3]{2,2500}|[4]{2,2500}|[5]{2,2500}|[6]{2,2500}|[7]{2,2500}|[8]{2,2500}|[9]{2,2500}'
temp = (re.findall(pattern, str(temp)))

i = 0
j = 0
for i in range(len(temp)):
    if j < len(temp[i]):
        j = len(temp[i])
result = list(filter(lambda x: (int(len(x) == j)), temp))
print(temp)
